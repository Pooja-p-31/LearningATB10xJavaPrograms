package ex_04_Incement_Decrement_Operator;

public class Lab_15_Pre_Increment {
    public static void main(String[] args) {

        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        // Exp and Result table
        // Line no | a | Result of b
        // 6 | 10 | NA
        // 7 | 11 | 11
        // 8 | NA | 11
        // 9 | 11 | NA

    }
}
